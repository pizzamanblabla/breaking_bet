<?php

namespace AppBundle\Entity;

/**
 * Event
 */
class Event
{
    /**
     * @var integer
     */
    private $teamFirstId;

    /**
     * @var integer
     */
    private $teamSecondId;

    /**
     * @var string
     */
    private $name;

    /**
     * @var \DateTime
     */
    private $date;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set teamFirstId
     *
     * @param integer $teamFirstId
     *
     * @return Event
     */
    public function setTeamFirstId($teamFirstId)
    {
        $this->teamFirstId = $teamFirstId;

        return $this;
    }

    /**
     * Get teamFirstId
     *
     * @return integer
     */
    public function getTeamFirstId()
    {
        return $this->teamFirstId;
    }

    /**
     * Set teamSecondId
     *
     * @param integer $teamSecondId
     *
     * @return Event
     */
    public function setTeamSecondId($teamSecondId)
    {
        $this->teamSecondId = $teamSecondId;

        return $this;
    }

    /**
     * Get teamSecondId
     *
     * @return integer
     */
    public function getTeamSecondId()
    {
        return $this->teamSecondId;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Event
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set date
     *
     * @param \DateTime $date
     *
     * @return Event
     */
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }

    /**
     * Get date
     *
     * @return \DateTime
     */
    public function getDate()
    {
        return $this->date;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
}
